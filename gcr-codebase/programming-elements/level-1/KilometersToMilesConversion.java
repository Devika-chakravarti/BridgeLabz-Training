public class KilometersToMilesConversion{
	public static void main(String args[]){

		double distance = 10.8;
		
		// Converting Distance from km to miles
		double resultantDistance = 10.8 * 1.6;

		// Displaying the calculated conversion 
		System.out.print("The distance " + distance + " km in miles is " + resultantDistance); 
	}
}